local function QONKBNJ_fake_script() -- TextButton_3.LocalScript 
	local script = Instance.new('LocalScript', TextButton_3)

	local button = script.Parent
	local player = game.Players.LocalPlayer
	local ReplicatedStorage = game:GetService("ReplicatedStorage")
	local SayMessageRequest = ReplicatedStorage.DefaultChatSystemChatEvents:FindFirstChild("SayMessageRequest", true)
	
	local isChatting = false
	local chatConnection
	local message = "GUEST 666 ARMY ASSEMBLE"
	
	local function toggleChat()
		if isChatting then
			isChatting = false
			if chatConnection then
				chatConnection:Disconnect()
			end
		else
			isChatting = true
			chatConnection = game:GetService("RunService").Heartbeat:Connect(function()
				if isChatting and SayMessageRequest then
					SayMessageRequest:FireServer(message, "All")
					wait(1.5)
				end
			end)
		end
	end
	
	button.MouseButton1Click:Connect(toggleChat)
	
end
