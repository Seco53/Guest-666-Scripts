local function CEHZ_fake_script() -- TextButton_10.LocalScript 
	local script = Instance.new('LocalScript', TextButton_10)

	local button = script.Parent
	local player = game.Players.LocalPlayer
	local spinning = false -- Track whether the character is spinning
	
	local function spinCharacter()
		local character = player.Character or player.CharacterAdded:Wait()
		local humanoidRootPart = character:WaitForChild("HumanoidRootPart")
	
		spinning = true
		while spinning do
			humanoidRootPart.CFrame = humanoidRootPart.CFrame * CFrame.Angles(0, math.rad(15), 0) -- Rotate around the Y-axis
			task.wait(0.001) -- Control the speed of rotation
		end
	end
	
	button.MouseButton1Click:Connect(function()
		if not spinning then
			spinCharacter() -- Start spinning
		else
			spinning = false -- Stop spinning
		end
	end)
	
end
